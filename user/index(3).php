<h1>Berikut ini 13 makanan sehat yang dapat kamu mulai konsumsi setiap harinya agar tubuh kuat dan kulitmu juga tetap sehat</h1>

<p>1. Oatmeal
    Oatmeal merupakan salah satu pilihan menu sehat yang cepat dan mudah sehingga dapat dikonsumsi setiap hari. Tak heran banyak yang menjadikan oatmeal sebagai menu sarapan. Tak hanya mengandung karbohidrat, oat juga mengandung protein dan serat sehingga sangat bagus untuk tubuh. </p>
    <img src="Oatmeal.jpeg" alt="Oatmeal">

<p>2. Susu
    Susu dikenal memiliki kandungan vitamin, mineral, dan protein yang cukup tinggi serta kandungan baik lainnya seperti vitamin D, iodium asam folat. Selain itu juga susu merupakan salah satu sumber kalsium terbaik bagi tubuh. Mengonsumsi susu dapat membantu mencegah  penyakit tulang. Atau kamu juga bisa mengonsumsi produk olahan dari susu seperti keju, atau yogurt. </p>
    <img src="gambar susu 2.jpg" alt="Susu" width="350"> 

<p>3. Yogurt
    Sebagai salah satu bentuk olahan dari susu, yogurt tentunya juga memiliki banyak manfaat untuk tubuh bahkan kecantikan wajah. Yogurt dikenal memiliki kandungan probiotik, kalsium, vitamin B12, dan zinc yang bermanfaat untuk usus dan sistem pencernaan. Kamu bisa mengonsumsi yogurt di pagi hari dengan buah dan granola untuk menjaga kesehatan metabolisme, atau bisa juga menjadikan yogurt sebagai masker wajah dengan mencampurnya dengan madu atau olive oil. Masker yogurt dapat membantu melawan jerawat dan tanda penuaan lho, Realfoodfam!</p>
    <img src="yogurt.webp" alt="yogurt" width="350">

<p>4. Ikan
    Selain daging dan ayam, ikan merupakan sumber protein yang tinggi untuk tubuh. Bahkan, ikan mengandung asam lemak omega-3 yang baik untuk pertumbuhan otak, vitamin D,  kalsium, dan fosfor yang baik untuk pertumbuhan tulang.</p>
    <img src="gambar ikan.jpeg" alt="ikan" width="350">

<p>5. Kacang-kacangan
    Kacang-kacangan mengandung serat, lemak baik dan magnesium yang baik untuk tubuh. Maka itu, mengonsumsi kacang-kacangan sebagai pilihan menu makanan sehat dapat membantu kesehatan jantung dan mencegah diabetes.</p> 
    <img src="gambar kacang dan biji bijian.jpeg" alt="kacang-kacangan" width="350">

<p>6. Ubi
    Ubi merupakan salah satu alternatif menu makanan menyehatkan pengganti nasi atau roti. Mengandung vitamin C, B6, kalium, dan mangan yang sangat baik untuk tubuh, ubi juga memiliki manfaat sebagai sumber energi dan baik untuk pencernaan lho, Realfoodfam!</p>
    <img src="gambar ubi.jpg" alt="ubi"width="350">

<p>7. Buah Jeruk
    Buah jeruk merupakan sumber vitamin C, selain itu buah ini juga mengandung serat dan asam folat yang baik untuk tubuh. Kandungan vitamin C nya membantu menjaga kekebalan tubuh dan menangkal radikal bebas. </p>
    <img src="gambar buah jeruk.jpg" alt="buah jeruk"width="350">

<p>8. Buah Alpukat
    Alpukat dikenal memiliki banyak kandungan seperti lemak baik, asam folat, vitamin dan mineral. Mengonsumsinya secara rutin dapat mencegah resiko penyakit kronis seperti diabetes, jantung. Kamu bisa menjadikan alpukat sebagai menu sarapan misalnya dengan membuat jus, atau sebagai topping roti bakar. </p>
    <img src="gambar buah alpukat.jpeg" alt="buah alpukat"width="350">

<p>9. Telur
    Telur memiliki banyak manfaat bagi kesehatan bahkan kecantikan, dalam 100gram telur terdapat protein, lemak, karbohidrat, zat besi, dan fosfor. Mengonsumsi telur dapat membantu menjaga kesehatan jantung, menjaga imun, hingga menurunkan berat badan. Tak hanya itu, telur merupakan makanan sehat dan bergizi juga dapat dimanfaatkan untuk kecantikan karena dapat mengurangi garis halus, menjaga kekencangan kulit wajah, dan mengurangi komedo lho, Realfoodfam!</p>
    <img src="gambar telur.jpeg" alt="telur"width="350">

<p>10. Minyak Zaitun
    Minyak zaitun atau olive oil dikenal dengan kandungan vitamin E nya yang baik untuk kulit. Minyak zaitun biasa dimanfaatkan sebagai minyak pijat karena menambah kelembapan. Minyak zaitun juga memiliki zat polifenol untuk melindungi kulit wajah dari kerusakan.</p>
    <img src="gambar minyak zaitun.jpeg" alt="minyak zaitun"width="350">

<p>11. Sayuran Hijau
    Sayuran hijau memiliki kandungan serat yang tinggi, serta kaya akan vitamin dan mineral.
    Mengonsumsi sayuran hijau, seperti sawi, bayam, kale, diyakini mampu membantu menjaga kesehatan mata. Karena kandungan lutein pada sayuran hijau membantu mengurangi resiko degenerasi akibat pertambahan usia </p>
    <img src="Manfaat-Kesehatan-Sayur-Pakcoy-yang-Perlu-Kamu-Ketahui-1024x683.jpg" alt="sayuran hijau"width="350">

<p>12. Bawang Putih
    Bahan makanan yang biasa dijadikan bumbu utama berbagai makanan ini ternyata merupakan
    Bawang putih yang biasa dijadikan salah satu bumbu dalam masakan, ternyata sumber vitamin C, B6, selenium, serat, dan juga mangan. Tak heran bawang putih banyak dimanfaatkan sebagai salah satu bahan obat tradisional. Bawang putih dipercaya dapat membantu menjaga tekanan darah dan mengurangi kolesterol dalam tubuh.</p>
    <img src="gambar bawang putih.jpg" alt="bawang putih"width="350">

