        <h1>Proses Teknologi Pangan dalam Menghasilkan Bahan Pangan</h1>

        <p>1. Sortasi<br>
            Proses pertama yang harus dilakukan untuk menghasilkan makanan yang layak konsumsi adalah pemilihan bahan makanan yang nantinya akan diolah. Produk yang nantinya akan dihasilkan dapat memiliki kualitas terbaik jika bahan mentah yang dipilih adalah produk terbaik, utuh, dan juga terjamin. Misalnya, saat pemilihan biji kopi akan dilakukan berdasarkan perbedaan proses perendaman atau dibedakan dari warnanya. Pemilihan biji kopi yang dilakukan berdasarkan warnanya biasanya akan disortir menggunakan mesin khusus yang sudah dilengkapi dengan sensor warna. Sedangkan pada proses perendaman, biji kopi yang masih mengapung akan dipisahkan dengan biji kopi yang terendam. Adapun contoh lain yaitu saat proses pemilihan buah nanas, biasanya akan disortir berdasarkan ukuran untuk memasuki proses pengalengan.
        </p>
        <img src="sortir.jpg" alt="Sortasi">

        <p>2. Pemotongan<br>
            Proses pemotongan makanan biasanya memerlukan ukuran yang presisi. Sehingga hal tersebut akan membutuhkan teknologi mesin pemotong makanan. Supaya lebih memudahkan untuk memotong dan membagi makanan yang cepat dan tepat sesuai dengan ukuran yang diinginkan. Misalnya, pemotongan roti yang menggunakan teknologi mesin pemotong di pabrik roti.
        </p>
        <img src="mesin pemotong makanan.webp" alt="mesin pemotong" width="350"> 

        <p>3. Pencucian<br>
            Setiap kali akan membuat makanan olahan, maka bahan mentahnya wajib melalui proses pencucian dan sterilisasi terlebih dahulu. Jika bahan mentah tidak melakukan pencucian dan sterilisasi terlebih dahulu, maka akan sangat berbahaya apabila dikonsumsi nantinya. Contohnya makanan yang penuh dengan kotoran atau debu bisa membuat orang yang mengonsumsinya menjadi keracunan dengan ditandai munculnya penyakit diare. Proses pencucian memiliki tujuan untuk membersihkan zat pengotor dan mempertahankan zat murni. Sekarang ini, ada metode MPN atau Most Probable Number yang bisa digunakan untuk mengecek jumlah mikroba yang ada di dalam bahan pangan. Sehingga hal tersebut bisa membantu dalam menentukan higienitas pada suatu pangan.
        </p>
        <img src="mesin-pencuci-makanan-tangki.jpg" alt="Pencucian" width="350">

        <p>4. Pengeringan<br>
            Proses pengeringan ini umumnya bertujuan untuk menghilangkan kadar air pada bahan pangan. Supaya bahan pangan tersebut tidak mudah rusak, busuk, dan tetap awet. Misalnya saja proses pengeringan yang dilakukan saat membuat kerupuk. Dimana kerupuk akan dijemur di bawah terik matahari agar bisa lebih gurih. Contoh lainnya yaitu singkong yang sudah ditumbuk kemudian dikeringkan untuk mendapatkan tepung tapiokanya.
        </p>
        <img src="pengeringan.jpg" alt="Pengeringan" width="350">

        <p>5. Pemanasan<br>
            Semua produk pangan olahan biasanya dibuat menggunakan proses pemanasan. Contohnya saja saat membuat nasi goreng, nasi tersebut seringkali akan ditambah kecap untuk menambah cita rasa. Dalam proses pemanasannya, gula yang ada di dalam kecap yang sudah ditaburkan ke nasi goreng akan mengalami karamelisasi. Hal tersebutlah yang membuat aroma nasi goreng menjadi lebih.
        </p> 
        <img src="gorengan.jpg" alt="proses pemanasan" width="350">

        <p>6. Pendinginan<br>
            Proses yang satu ini sudah ada sejak zaman nenek moyang dulu. Dimana proses pendinginan tersebut dikethaui bermanfaat untuk mengawetkan sayur daging, buah, dan lainnya. Adapun beberapa produk dari pangan olahan dan juga awetan yang membutuhkan proses pendinginan. Misalnya soft ice cream yang sempat ngetren pada tahun 2010 an. Es krim tersebut memiliki tekstur yang sangat lembut. Namun, es krim tersebut masih memerlukan teknologi yang bisa melakukan proses pembekuan dengan cepat guna mencegah proses kristalisasi. Begitu juga dengan makanan-makanan yang disajikan di dalam pesawat, proses pendinginan harus dilakukan dengan kecepatan yang tinggi.
        </p>
        <img src="pendinginan makanan.jpeg" alt="pendinginan makanan" width="350">

        <p>7. Pengemasan<br>
            Pada proses pengemasan ini harus dibuat berbeda dan disesuaikan dengan jenis makanan. Hal tersebut bertujuan untuk mencegah kerusakan pangan. Jika produk adalah olahan daging, misalnya sosis atau bakso, maka keseluruhan kemasan harus rapat tanpa adanya udara sedikitpun. Caranya bisa menggunakan vacuum untuk mengatasi kadar oksigen. Kemasan kedap udara bertujuan untuk mencegah adanya kerusakan pada makanan. Begitu juga dengan berbagai bungkusan makanan ringan yang dibuat kembung. Hal tersebut bertujuan agar makanan yang ada di dalamnya tidak hancur.
        </p>
