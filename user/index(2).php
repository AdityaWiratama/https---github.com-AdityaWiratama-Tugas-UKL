        <h1>Pentingnya Mengkonsumsi Makanan yang Bermanfaat</h1>
        <p>Agar tubuh tetap sehat hingga usia senja, pastinya memerlukan berbagai asupan gizi untuk menjalankan fungsinya. Meski pada dasarnya kebutuhan nutrisi anak-anak sama dengan orang dewasa, tapi pada usia-usia tertentu tubuh memerlukan asupan tambahan. Nah, berikut konsumsi makanan yang ideal sesuai usia.</p>

        <p>1. Kebutuhan Nutrisinya Sama<br>
        Kata ahli, pada dasarnya kebutuhan nutrisi anak-anak sama dengan orang dewasa. Yang perlu diperhatikan, lebih mengarah pada porsi yang dibutuhkan. Nah, berikut nutrisi yang dibutuhkan oleh anak-anak dan orang dewasa.</p> 

        <p>- Karbohidrat<br>
        Karbohidrat merupakan sumber energi yang diperlukan tubuh. Karbohidrat juga berperan dalam membantu tubuh menggunakan protein dan lemak untuk membangun dan memperbaiki jaringan tubuh. Kamu bisa mendapatkan sumber karbohidrat dari beras, sereal, roti, hingga kentang.</p>

        <p>- Lemak<br>
        Yang satu ini merupakan sumber energi lain yang mudah disimpan dalam tubuh anak-anak. Ibu bisa mencari sumber lemak dari susu, ikan, daging merah, dan kacang-kacangan.</p>

        <p>- Protein<br>
        Kata ahli, protein amat diperlukan untuk perkembangan anak. Protein sendiri merupakan bahan utama penyusun sel dalam tubuh manusia. Kamu bisa mencari sumber protein dari susu, telur, makanan laut, daging, kacang-kacangan, sayuran, dan biji-bijian.</p>

        <p>- Kalsium<br>
        Kalsium merupakan nutrisi yang mendukung pertumbuhan anak, seperti memperkuat tulang dan gigi. Terpenuhinya kebutuhan kalsium juga bisa mencegah dirimu mengalami masalah yang berkaitan dengan tulang, seiring bertambahnya usia. Kalsium bisa didapat dari susu dan produk olahan lainnya, seperti yogurt dan keju.
        Selain itu, masih banyak lagi nutrisi yang dibutuhkan oleh anak-anak hingga manula, yaitu serat, zat besi, antioksidan, asam folat, hingga vitamin.</p>

        <p>2. Asupan Tambahan Sesuai Usia</p>

        <p>- Usia Remaja<br>
        Remaja pria dan wanita disarankan untuk lebih banyak mengonsumsi makanan tinggi kalsium. Tujuannya jelas, untuk menjaga kesehatan dan kekuatan tulang di usia selanjutnya. Selain itu, remaja wanita juga disarankan untuk lebih banyak mengonsumsi zat besi. Pasalnya, mereka akan lebih banyak kehilangan darah karena menstruasi. Zat besi ini bisa didapat dari daging merah, telur, kacang, sayuran hijau, dan buah-buahan.</p>

        <p>- Usia 20-an<br>
        Kata ahli, usia ini amat rentan dengan gaya hidup yang tak sehat. Misalnya, konsumsi minuman beralkohol, merokok, hingga konsumsi junk food. Lalu, nutrisi apa yang lebih dibutuhkan pada usia 20-an? Sebaiknya perbanyaklah konsumsi buah yang kaya vitamin C yang bisa menjaga sistem imun dan kesehatan tubuh. Contohnya, seperti buah berry, jeruk, dan lain-lain.</p>

        <p>- Usia 30-an<br>
        Di usia ini umumnya baik pria maupun wanita sudah memiliki banyak aktivitas, mulai dari bekerja hingga urusan rumah tangga. Sayangnya, banyak pula orang di usia ini yang melupakan kebutuhan nutrisi karena keterbatasan waktu. Kata ahli, di usia 30-an sebaiknya kamu perlu menghindari makanan yang mengandung tinggi gula, seperti donat atau cupcake. Para ahli menganjurkan untuk lebih banyak mengonsumsi makanan yang kaya vitamin B untuk melawan stres dan penambah energi. Misalnya, telur, sayuran hijau.</p>

        <p>- Usia 40-an<br>
        Khusus untuk wanita, masa ini mendekati masa menopause dimana tubuh akan kehilangan massa otot karena perubahan hormonal. Menurunnya estrogen di masa ini juga bisa meningkatkan risiko serangan jantung dan osteoporosis. Oleh sebab itu, para ahli menganjurkan wanita di usia 40-an untuk mengonsumsi almond karena mengandung lemak sehat yang baik untuk kesehatan jantung. Selain itu, kamu juga disarankan untuk mengonsumsi beras merah dan oats yang juga baik untuk jantung.</p>

        <p>- Usia 50-an<br>
        Wanita di usia ini memiliki risiko penyakit kardiovaskular yang semakin tinggi. Kekurangan hormon estrogen bisa membuat wanita berpeluang mengalami penyakit jantung. Oleh sebab itu para ahli menganjurkan wanita di usia 50-an untuk rajin memonitor tekanan darah, berat badan, dan kadar kolesterol. Selain itu, para ahli juga menyarankan untuk rutin mengonsumsi kedelai, tahu, dan kacang-kacangan agar efek samping menopause berkurang. Akan lebih baik lagi, bila kamu juga rutin mengonsumsi susu agar terhindar dari osteoporosis.</p>

